export type MessageKey = keyof (typeof messages)["en"];

export function localizeProfile(profile: Profile, locale: Locale): Profile {
  return { ...profile, ...profile.translations?.[locale] };
}

export function localizeProject(project: Project, locale: Locale): Project {
  const translated = project.translations?.[locale];
  if (!translated) return project;
  return {
    ...project,
    ...translated,
    // Keep canonical English status for color/status logic consumers when needed.
    status: translated.status ?? project.status,
  };
}

export function localizeExperience(
  experience: Experience,
  locale: Locale,
): Experience {
  return { ...experience, ...experience.translations?.[locale] };
}

export function localizeCertificate(
  certificate: Certificate,
  locale: Locale,
): Certificate {
  return { ...certificate, ...certificate.translations?.[locale] };
}

export function localizeNavItem(item: NavItem, locale: Locale): NavItem {
  return { ...item, ...item.translations?.[locale] };
}
